//program to demonstrate bitwise and shifting operator 
package org.tnsif.operator;

public class BitwiseOperatorDemo {

	public static void main(String[] args) {
		int x=13, y=9;
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		System.out.println(8>>1);
		System.out.println(8<<1);

	}

}
